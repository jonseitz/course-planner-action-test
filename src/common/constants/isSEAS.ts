/**
 * Represents possible values for the "Is Seas?" column on courses.
 */

enum IS_SEAS {
  /** This course is offered by SEAS */
  Y = 'Y',
  /** This course is not offered by SEAS */
  N = 'N',
  /** This course is offered by Earth & Planetary Sciences */
  EPS = 'EPS',
}

export default IS_SEAS;
